package org.ifgi.sla.evaluator.type;

public class InitialResponseType
{
	public int[] day;
	public int[] week;
	public int[] month;
	public int[] year;
	
	public int[] getDay()
	{
		return day;
	}

	public void setDay(int[] day)
	{
		this.day = day;
	}

	public int[] getWeek()
	{
		return week;
	}

	public void setWeek(int[] week)
	{
		this.week = week;
	}

	public int[] getMonth()
	{
		return month;
	}

	public void setMonth(int[] month)
	{
		this.month = month;
	}

	public int[] getYear()
	{
		return year;
	}

	public void setYear(int[] year)
	{
		this.year = year;
	}
}
