package org.ifgi.sla.evaluator.type;

public class PixelType
{
	public long day;
	public long week;
	public long month;
	public long year;
	
	public long getDay()
	{
		return day;
	}
	
	public void setDay(long day)
	{
		this.day = day;
	}
	
	public long getWeek()
	{
		return week;
	}
	
	public void setWeek(long week)
	{
		this.week = week;
	}
	
	public long getMonth()
	{
		return month;
	}
	
	public void setMonth(long month)
	{
		this.month = month;
	}
	
	public long getYear()
	{
		return year;
	}
	
	public void setYear(long year)
	{
		this.year = year;
	}
	
	
}
