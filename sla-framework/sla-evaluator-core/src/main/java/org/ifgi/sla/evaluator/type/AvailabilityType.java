package org.ifgi.sla.evaluator.type;

public class AvailabilityType
{
	public double day;
	public double week;
	public double month;
	public double year;
	
	public double getDay()
	{
		return day;
	}
	
	public void setDay(double day)
	{
		this.day = day;
	}
	
	public double getWeek()
	{
		return week;
	}
	
	public void setWeek(double week)
	{
		this.week = week;
	}
	
	public double getMonth()
	{
		return month;
	}
	
	public void setMonth(double month)
	{
		this.month = month;
	}
	
	public double getYear()
	{
		return year;
	}
	
	public void setYear(double year)
	{
		this.year = year;
	}
	
	public double day(int numberOfDays)
	{
		return year;
	}
}
